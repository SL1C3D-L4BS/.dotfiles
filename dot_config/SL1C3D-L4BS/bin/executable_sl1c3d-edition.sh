#!/usr/bin/env bash
# SL1C3D-L4BS — Edition resolver (core + edition + host overlays)
#
# Generates:
# - ~/.config/hypr/edition.conf
# - ~/.config/SL1C3D-L4BS/state/edition.json
#
# Strict local-only secrets: this tool does not read or write secrets.

set -euo pipefail

usage() {
  cat <<'EOF'
Usage:
  sl1c3d-edition set <edition>
  sl1c3d-edition get
  sl1c3d-edition list
  sl1c3d-edition apply

Editions are defined by Hypr overlay files at:
  ~/.config/hypr/editions/<edition>.conf
EOF
}

CFG_DIR="${XDG_CONFIG_HOME:-$HOME/.config}"
STATE_DIR="$CFG_DIR/SL1C3D-L4BS/state"
HYPR_DIR="$CFG_DIR/hypr"
EDITION_FILE="$STATE_DIR/edition"
EDITION_JSON="$STATE_DIR/edition.json"
HYPR_EDITION_CONF="$HYPR_DIR/edition.conf"

mkdir -p "$STATE_DIR" "$HYPR_DIR"

list_editions() {
  if [ -d "$HYPR_DIR/editions" ]; then
    (ls -1 "$HYPR_DIR/editions" 2>/dev/null || true) | sed 's/\.conf$//' | sort -u
  fi
}

write_outputs() {
  local edition="$1"
  local overlay="$HYPR_DIR/editions/$edition.conf"

  if [ ! -f "$overlay" ]; then
    echo "Unknown edition: $edition"
    echo "Known editions:"
    list_editions | sed 's/^/  - /'
    exit 2
  fi

  printf '%s' "$edition" >"$EDITION_FILE"

  cat >"$HYPR_EDITION_CONF" <<EOF
# Generated by sl1c3d-edition (do not edit)
source = $overlay
EOF

  cat >"$EDITION_JSON" <<EOF
{
  "edition": "$(printf '%s' "$edition" | sed 's/\"/\\"/g')"
}
EOF

  echo "Applied edition: $edition"
  echo "  hypr: $HYPR_EDITION_CONF -> $overlay"
  echo "  state: $EDITION_JSON"
}

cmd="${1:-}"
case "$cmd" in
  set)
    [ $# -eq 2 ] || { usage; exit 2; }
    write_outputs "$2"
    ;;
  get)
    if [ -f "$EDITION_FILE" ]; then
      cat "$EDITION_FILE"
    else
      echo "base"
    fi
    ;;
  list)
    list_editions
    ;;
  apply)
    edition="$(sl1c3d-edition get)"
    write_outputs "$edition"
    ;;
  *)
    usage
    exit 2
    ;;
esac

